	   COUNTRYCODE AS CountryCode,
	   FIRSTNAME AS FirstName,
	   LASTNAME AS LastName,
	   EMAIL AS Email,
	   ADDRESS1 AS Address1,
	   ADDRESS2 AS Address2,
	   ADDRESS3 AS Address3,
	   LANDMARK AS LandMark,
	   BANKNAME AS BankName,
	   ACCOUNTNUMBER AS AccountNumber
FROM EMPLOYEE
